const LOCALES: Record<Locale, Translations> = { en: EN, ar: AR, so: SO };

interface I18nContextType {
  locale: Locale;
  setLocale: (l: Locale) => void;
  t: (key: string, fallback?: string) => string;
  isRTL: boolean;
}

const I18nContext = createContext<I18nContextType>({
  locale: "so",
  setLocale: () => {},
  t: (k, f) => f ?? k,
  isRTL: false,
});

const STORAGE_KEY = "al-bayaan-locale";

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>(() => {
    const saved = localStorage.getItem(STORAGE_KEY) as Locale | null;
    // Default is Somali — only respect saved value if it's a valid locale
    return saved && ["en", "ar", "so"].includes(saved) ? saved : "so";
  });

  const isRTL = locale === "ar";

  useEffect(() => {
    localStorage.setItem(STORAGE_KEY, locale);
    document.documentElement.lang = locale;
    document.documentElement.dir = isRTL ? "rtl" : "ltr";
  }, [locale, isRTL]);

  const setLocale = (l: Locale) => setLocaleState(l);

  const t = (key: string, fallback?: string): string => {
    // Check the active locale first, then fall back to the key itself
    // (no silent English fallback when Somali or Arabic is selected)
    return LOCALES[locale]?.[key] ?? fallback ?? key;
  };

  return (
    <I18nContext.Provider value={{ locale, setLocale, t, isRTL }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  return useContext(I18nContext);
}
